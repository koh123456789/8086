.MODEL SMALL
.STACK 100
.DATA

    TOTALAMOUNT DW 423
    HUN DB 100
    TEN DB 10

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

	MOV AX,TOTALAMOUNT
	DIV HUN
	MOV BX,AX

	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H

	MOV AX,0H 
	MOV AL,BH
	DIV TEN
	MOV BX,AX

	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H

	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H

    MOV AX,4C00H
    INT 21H
MAIN ENDP
END MAIN
